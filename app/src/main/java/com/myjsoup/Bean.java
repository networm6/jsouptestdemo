package com.myjsoup;

public class Bean
{
	private String 书名,作者,阅读,简介,图片,跳转;


	public void set书名(String 书名)
	{
		this.书名 = 书名;
	}

	public String get书名()
	{
		return 书名;
	}

	public void set作者(String 作者)
	{
		this.作者 = 作者;
	}

	public String get作者()
	{
		return 作者;
	}

	public void set阅读(String 阅读)
	{
		this.阅读 = 阅读;
	}

	public String get阅读()
	{
		return 阅读;
	}

	public void set简介(String 简介)
	{
		this.简介 = 简介;
	}

	public String get简介()
	{
		return 简介;
	}

	public void set图片(String 图片)
	{
		this.图片 = 图片;
	}

	public String get图片()
	{
		return 图片;
	}

	public void set跳转(String 跳转)
	{
		this.跳转 = 跳转;
	}

	public String get跳转()
	{
		return 跳转;
	}
}
